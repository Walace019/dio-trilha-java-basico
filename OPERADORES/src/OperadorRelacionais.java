public class OperadorRelacionais {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int c = 3;

        System.out.println(a > b); // a Maior que b
        System.out.println(a >= b); // a Maior ou Igual a b
        System.out.println(a < b); // a é Menor que b
        System.out.println(a <= b);// a é Menor ou Igual a b
        System.out.println(a == b); //a é  Igual a b
        System.out.println(a != b); // Diferente de b
    }
}
