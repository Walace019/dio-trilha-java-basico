public class OperadorUnario {
    public static void main(String[] args) {
        int numero = 5;
        System.out.println(numero + numero); // Operador unário de valor positivo – números são positivos sem esse operador explicitamente //
        System.out.println(numero - numero); // Operador unário de valor negativo – nega um número ou expressão aritmética //
        System.out.println(++ numero); //Operador unário de incremento de valor – incrementa o valor em 1 unidade //
        System.out.println(-- numero); // Operador unário de decremento de valor – decrementa o valor em 1 unidade //

    boolean variavel = true;
        System.out.println(!variavel); //Operador unário lógico de negação – nega o valor de uma expressão booleana
    }
}
